function SyntaxHighlighter({ content }) {
    if (!content) return null;

    const lines = content.split('\n');

    // Very basic "highlighter" using simple rules
    const highlight = (line) => {
        // Comments
        if (line.trim().startsWith('//')) return <span className="text-zinc-500 italic">{line}</span>;
        if (line.trim().startsWith('#')) return <span className="text-blue-400 font-bold">{line}</span>;

        // Keywords
        const keywords = ['import', 'export', 'default', 'function', 'const', 'return', 'from', 'if', 'else', 'true', 'false'];

        let elements = [];

        const words = line.split(/(\s+|[{}()[\],./:;<>!=+*-])/);

        words.forEach((word, i) => {
            if (keywords.includes(word)) {
                elements.push(<span key={i} className="text-[#c678dd]">{word}</span>);
            } else if (word.startsWith('"') || word.startsWith("'") || word.startsWith("`")) {
                elements.push(<span key={i} className="text-[#98c379]">{word}</span>);
            } else if (!isNaN(word) && word !== "") {
                elements.push(<span key={i} className="text-[#d19a66]">{word}</span>);
            } else if (/^[A-Z][a-zA-Z0-9]*$/.test(word)) {
                elements.push(<span key={i} className="text-[#e5c07b]">{word}</span>);
            } else if (word === '{' || word === '}' || word === '(' || word === ')' || word === '[' || word === ']') {
                elements.push(<span key={i} className="text-[#abb2bf]">{word}</span>);
            } else {
                elements.push(word);
            }
        });

        return elements;
    };

    return (
        <div className="flex w-full">
            {/* Line Numbers */}
            <div className="w-12 pr-4 text-right select-none border-r border-white/5 mr-6 py-1">
                {lines.map((_, i) => (
                    <div key={i} className="text-[#3b3b4f] text-[11px] leading-[22px]">{i + 1}</div>
                ))}
            </div>
            {/* Code */}
            <div className="flex-1 py-1 overflow-x-auto custom-scrollbar">
                {lines.map((line, i) => (
                    <div key={i} className="whitespace-pre leading-[22px] min-h-[22px]">
                        {highlight(line)}
                    </div>
                ))}
            </div>
        </div>
    );
}

