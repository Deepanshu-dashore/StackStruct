function FileTreeNode({ node, onFileSelect, selectedFile, depth = 0 }) {
    const [isOpen, setIsOpen] = useState(node.open || false);
    const isFolder = node.type === 'folder';
    const isSelected = selectedFile?.name === node.name;

    const handleClick = () => {
        if (isFolder) {
            setIsOpen(!isOpen);
        } else {
            onFileSelect(node);
        }
    };

    return (
        <div className="select-none">
            <div
                onClick={handleClick}
                className={`flex items-center gap-2.5 py-1 px-3 rounded-md cursor-pointer transition-all duration-200 group ${isSelected ? 'bg-white/5 text-blue-400' : 'text-white/30 hover:text-white/70 hover:bg-white/[0.03]'
                    }`}
                style={{ paddingLeft: `${depth * 16 + 8}px` }}
            >
                {isFolder ? (
                    <ChevronRight className={`h-3 w-3 transition-transform ${isOpen ? 'rotate-90 text-white/60' : 'text-white/10'} group-hover:text-blue-400/50`} />
                ) : (
                    <div className="w-3" />
                )}
                <Icon icon={node.icon} className={`h-4 w-4 shrink-0 transition-all duration-300 ${isFolder ? 'text-blue-500/60 group-hover:text-blue-400' :
                        isSelected ? 'text-blue-400' : 'text-white/20 group-hover:text-white/40'
                    } group-hover:scale-110`} />
                <span className={`text-[12px] font-medium tracking-tight whitespace-nowrap transition-colors ${isSelected ? 'text-blue-400/90' : ''}`}>
                    {node.name}
                </span>
            </div>
            {isFolder && isOpen && node.children && (
                <div className="mt-0.5">
                    {node.children.map((child, idx) => (
                        <FileTreeNode
                            key={`${child.name}-${idx}`}
                            node={child}
                            onFileSelect={onFileSelect}
                            selectedFile={selectedFile}
                            depth={depth + 1}
                        />
                    ))}
                </div>
            )}
        </div>
    );
}
